package com.mukulbhardwaj1313.introslider;

import android.content.Context;

public class Constants {

    public static float one_dp(Context context){
        return context.getResources().getDimension(R.dimen.oneDp);
    }
    public static float one_sp(Context context){
        return context.getResources().getDimension(R.dimen.oneSp);
    }
}
